#Escape Sequence

# 1) \ tells to ignore 1 word after it 
weather = "It's \"kind of\"  Sunny"

print(weather) #n It's "kind of"  Sunny

time = "It's 2021\\12\\12" 
print(time)  #It's 2021\12\12

# 2) \t  tells to add a tab

print ("\t hello")

# 3) \n tells to add to the new license
print ("hello \n what's up? ")